# [PCCE 기출문제] 8번 / 닉네임 규칙 (디버깅 문제)
# https://school.programmers.co.kr/learn/courses/30/lessons/340200

def solution(nickname):
    answer = ""
    for letter in nickname:
        if letter == "l":
            answer += "I"
        elif letter == "w":
            answer += "vv"
        elif letter == "W":
            answer += "VV"
        elif letter == "O":
            answer += "0"
        else:
            answer += letter
    if len(answer) < 3:
        answer += "o" * (4 - len(answer)) # answer += "o"
    if len(answer) > 8:
        answer = answer[:8]
    return answer